    private void btningresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btningresarActionPerformed
        // TODO add your handling code here:

        AdministradorDAO modeloAdminDAO = new AdministradorDAO();
        Administrador modeloAdmin = new Administrador();
        
        Empleado modeloEmpleado = new Empleado();
        EmpleadoDAO modeloEmpleadoDAO = new EmpleadoDAO();

        String pass = new String(txtpassword.getPassword());

        modeloAdmin.setUsuario(txtusuario.getText());
        modeloAdmin.setPassword(txtpassword.getText());

        modeloEmpleado.setNombre(modeloAdmin.getNombre());
        
        
        try {
            if (modeloAdminDAO.login2(modeloAdmin)) {

                this.dispose();
                ImageIcon miicono = new ImageIcon(this.getClass().getResource("/imagenes/bienvenido.png"));
                JOptionPane.showMessageDialog(null, "-BIENVENIDO AL HOTEL AZTECA-", "Credenciales Correctas",
                        JOptionPane.INFORMATION_MESSAGE, miicono);
                FormPrincipal inicio = new FormPrincipal();
                inicio.setVisible(true);
                
                inicio.lblusuario.setText(modeloAdmin.getUsuario());
                inicio.lblacceso.setText(modeloAdmin.getAcceso());
                inicio.lblidempleado.setText(String.valueOf(modeloAdmin.getIdempleado()));
                
                if (!inicio.lblacceso.getText().equals("Administrador")) {
                    inicio.submenuadmin.setEnabled(false);
                    inicio.submenuempleados.setEnabled(false);
                    inicio.submenuhabitacion.setEnabled(false);
                    inicio.submenuservextra.setEnabled(false);
                    
                    
                }

                
            } 
            
            else {

                ImageIcon miicono = new ImageIcon(this.getClass().getResource("/imagenes/denegado.png"));
                JOptionPane.showMessageDialog(null, "Usuario No Registrado", "Credenciales Incorrectas",
                        JOptionPane.INFORMATION_MESSAGE, miicono);

            }
        } catch (Exception e) {
            System.err.println(e);
        }

    }//GEN-LAST:event_btningresarActionPerformed

